DROP TABLE IF EXISTS `goods`;
CREATE TABLE `goods` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `goods_name` varchar(50) DEFAULT NULL,
  `order_taker` varchar(50) DEFAULT NULL,
  `publish_man` varchar(50) DEFAULT NULL,
  `status` int(11) NOT NULL DEFAULT '0',
  `start_place` varchar(50) DEFAULT NULL,
  `start_province` varchar(20) DEFAULT NULL,
  `start_city` varchar(20) DEFAULT NULL,
  `start_area` varchar(20) DEFAULT NULL,
  `start_place_detail` varchar(255) DEFAULT NULL,
  `end_place` varchar(50) DEFAULT NULL,
  `end_province` varchar(20) DEFAULT NULL,
  `end_city` varchar(20) DEFAULT NULL,
  `end_area` varchar(20) DEFAULT NULL,
  `end_place_detail` varchar(255) DEFAULT NULL,
  `car_long` varchar(50) DEFAULT NULL,
  `car_type` varchar(50) DEFAULT NULL,
  `price_source` varchar(10) DEFAULT NULL,
  `load_type` varchar(20) DEFAULT NULL,
  `load_time` bigint(20) DEFAULT NULL,
  `pay_type` varchar(10) DEFAULT NULL,
  `price` int(11) DEFAULT NULL,
  `comment` varchar(150) DEFAULT NULL,
  `goods_weight` int(11) DEFAULT NULL,
  `goods_volume` int(11) DEFAULT NULL,
  `create_time` datetime DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=28 DEFAULT CHARSET=utf8;
